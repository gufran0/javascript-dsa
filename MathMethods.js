// Using the remainder (modulus) operator to find the remainder of a division
// Example: Finding the remainder when 13 is divided by 2
const remainder = 13 % 2;
console.log(`The remainder of 13 divided by 2 is: ${remainder}`); // Output: 1

// Using the exponentiation operator (**) to raise a number to a power
// Example: Raising 5 to the power of 2
const result = 5 ** 2;
console.log(`5 raised to the power of 2 is: ${result}`); // Output: 25

// Using Math.PI to get the value of Pi
console.log(`The value of Pi is: ${Math.PI}`); // Output: 3.141592653589793

// Using Math.sqrt to calculate the square root of a number
// Example: Calculating the square root of 16
console.log(`The square root of 16 is: ${Math.sqrt(16)}`); // Output: 4
// Example: Calculating the square root of 26
console.log(`The square root of 26 is: ${Math.sqrt(26)}`); // Output: 5.0990195135927845

// Using Math.pow to raise a number to a power
// Example: Raising 2 to the power of 3
console.log(`2 raised to the power of 3 is: ${Math.pow(2, 3)}`); // Output: 8

// Using Math.floor to round a number down to the nearest integer
// Example: Rounding 2.9 down to the nearest integer
console.log(`2.9 rounded down is: ${Math.floor(2.9)}`); // Output: 2
// Example: Rounding 7.6 down to the nearest integer
console.log(`7.6 rounded down is: ${Math.floor(7.6)}`); // Output: 7
// Example: Rounding -3.2 down to the nearest integer
console.log(`-3.2 rounded down is: ${Math.floor(-3.2)}`); // Output: -4

// Using Math.ceil to round a number up to the nearest integer
// Example: Rounding 2.8 up to the nearest integer
console.log(`2.8 rounded up is: ${Math.ceil(2.8)}`); // Output: 3

// Using Math.round to round a number to the nearest integer
// Example: Rounding 2.6 to the nearest integer
console.log(`2.6 rounded is: ${Math.round(2.6)}`); // Output: 3

// Using Math.random to generate a random number between 0 and 1
// Example: Generating a random number and multiplying by 20
const randomNum = Math.random() * 20;
console.log(`Random number between 0 and 20: ${randomNum}`);

// Using Math.floor to round down the random number
console.log(`Random number rounded down: ${Math.floor(randomNum)}`);

// Using Math.max to find the maximum of a set of numbers
// Example: Finding the maximum between 2 and 3
const max = Math.max(2, 3);
console.log(`The maximum between 2 and 3 is: ${max}`); // Output: 3

// Note: Dividing a number by zero results in Infinity
// Example: 8 divided by 0
console.log(`8 divided by 0 is: ${8 / 0}`); // Output: Infinity

// Note: Dividing zero by zero results in NaN (Not-a-Number)
// Example: 0 divided by 0
console.log(`0 divided by 0 is: ${0 / 0}`); // Output: NaN
